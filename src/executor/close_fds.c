#include "../../includes/minishell.h"

void	close_fds(int *fd)
{
	if (fd)
	{
		if (fd && fd[0] != -1 && fd[0] != -2)
			close(fd[0]);
		if (fd && fd[1] != -1 && fd[1] != -2)
			close(fd[1]);
	}
}
