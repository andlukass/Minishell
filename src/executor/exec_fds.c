#include "../../includes/minishell.h"

void	close_fds(int *fd)
{
	if (fd)
	{
		if (fd && fd[0] != -1 && fd[0] != -2)
			close(fd[0]);
		if (fd && fd[1] != -1 && fd[1] != -2)
			close(fd[1]);
	}
}

void	close_fd(int fd)
{
	if (fd != -1)
		close(fd);
}

void	init_fds(int (*fd1)[2], int (*fd2)[2])
{
	if (fd1)
	{
		(*fd1)[0] = -1;
		(*fd1)[1] = -1;
	}
	if (fd2)
	{
		(*fd2)[0] = -1;
		(*fd2)[1] = -1;
	}
}
